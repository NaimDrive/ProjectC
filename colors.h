#ifndef __COLORS_H__
#define __COLORS_H__

/*
    Strings printed after this function will be red.
    @Return void
*/
void red();

/*
    Strings printed after this function will be green.
    @Return void
*/
void green();

/*
    Strings printed after this function will be have a green background.
    @Return void
*/
void greenBG();

/*
    Strings printed after this function will be yellow.
    @Return void
*/
void yellow();

/*
    Strings printed after this function will be have a green background.
    @Return void
*/
void yellowBG();

/*
    Strings printed after this function will be blue.
    @Return void
*/
void blue();

/*
    Strings printed after this function will be magenta.
    @Return void
*/
void purple();

/*
    Strings printed after this function will be have a magenta background.
    @Return void
*/
void purpleBG();

/*
    Strings printed after this function will be have a white background.
    @Return void
*/
void whiteBG();

/*
    Strings printed after this function will be a bright white.
    @Return void
*/
void white();

/*
    Strings printed after this function will be gray.
    @Return void
*/
void gray();

/*
    Strings printed after this function will be have a gray background.
    @Return void
*/
void grayBG();

/*
    Strings printed after this function will be as usual (default).
    @Return void
*/
void resetColor();

#endif