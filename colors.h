#ifndef __COLORS_H__
#define __COLORS_H__

/*
    Strings printed after this function will be red.
    @Return void
*/
void red();

/*
    Strings printed after this function will be green.
    @Return void
*/
void green();

/*
    Strings printed after this function will be yellow.
    @Return void
*/
void yellow();

/*
    Strings printed after this function will be blue.
    @Return void
*/
void blue();

/*
    Strings printed after this function will be magenta.
    @Return void
*/
void magenta();

/*
    Strings printed after this function will be brown.
    @Return void
*/
void brown();

/*
    Strings printed after this function will be a bright white.
    @Return void
*/
void white();

/*
    Strings printed after this function will be black.
    @Return void
*/
void black();

/*
    Strings printed after this function will be as usual (default).
    @Return void
*/
void resetColor();

#endif